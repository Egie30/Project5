UPDATE PAYROLL
SET ACTG_TYP = 3
	WHERE PRSN_NBR IN (
		SELECT 
			PPL.PRSN_NBR
		FROM PAY.PAYROLL PYRL 
		LEFT JOIN CMP.PEOPLE PPL 
			ON PYRL.PRSN_NBR = PPL.PRSN_NBR 
		LEFT JOIN CMP.POS_TYP POS
			ON PPL.POS_TYP = POS.POS_TYP
		WHERE YEAR(PYRL.PYMT_DTE) = 2017
			AND PPL.CO_NBR = 271
			#AND SUBSTRING(POS.SEC_KEY,10,1) <= 7
		GROUP BY PYRL.PRSN_NBR
	)
	AND YEAR(PYMT_DTE) = 2017
	
